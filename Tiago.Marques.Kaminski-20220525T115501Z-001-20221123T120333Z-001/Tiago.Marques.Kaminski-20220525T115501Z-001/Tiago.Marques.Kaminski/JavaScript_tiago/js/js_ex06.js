var dia 
dia=parseInt( prompt("Digite um numero correpondente ao dia da semana: "))

switch (dia){

    case 1:
    {
        alert("Domingo")
    }
    case 2:
    {
        alert("segunda-feira")
    }
    case 3:
    {
        alert("Terça-feira")
    }
    case 4:
    {
        alert("Quarta-feira")
    }
    case 5:
    {
        alert("Quinta-feira")
    }
    case 6:
    {
        alert("Sexta-feira")
    }
    case 7:
    {
        alert("Sábado")
        
      
    }
    default:{
        alert("opção inválida")
    }
}
